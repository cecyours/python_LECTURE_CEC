
## bitwise

a = 5
b = 3

######### bitwise AND
'''		
	a : 0 1 0 1
	b :	0 0 1 1
	------------
	& : 0 0 0 1 	=> 1
  	-------------

'''
c = a&b
print(a,"&",b,"=",c)

print("-------------------")

######### bitwise OR
'''		
	a : 0 1 0 1
	b :	0 0 1 1
	------------
	| : 0 1 1 1 	=> 7
  	-------------

'''
c = a|b
print(a,"|",b,"=",c)

print("-------------------")

######### bitwise EX-OR
'''		
	a : 0 1 0 1
	b :	0 0 1 1
	------------
	^ : 0 1 1 0 	=> 6
  	-------------

'''
c = a^b
print(a,"^",b,"=",c)

print("-------------------")

### negation

'''
		c = -(a+1)
		  = -6
'''
c = ~a
print("~",a,"=",c)

print("-------------------")

## left shift

'''
	c = a*2^b
	  = 5 * 2^3
	  =5 * 8
'''

c = a<<b
print(a,"<<",b,"=",c)

print("-------------------")

a = 16
## right shift

'''
	c = a/2^b
	  = 16 / 2^3
	  = 16 / 8
	  =2
'''

c = a>>b
print(a,">>",b,"=",c)